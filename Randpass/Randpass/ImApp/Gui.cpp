void Gui::framebuffer_size_callback(GLFWwindow* window, int width, int height)
{
    Renderer renderer;  // Create an instance of the Gui class
    Application app;  // Create an instance of the Gui class

    renderer.StartDraw(true);
    app.Update();
    renderer.EndDraw(true);

    glfwSwapBuffers(window);
    glfwPollEvents();
}

void Gui::StartDraw(bool useMain)
{
    renderer.StartDraw(useMain);
}

void Gui::EndDraw(bool useMain)
{
    renderer.EndDraw(useMain);

    // glfw: swap buffers and poll IO events (keys pressed/released, mouse moved etc.)
    // -------------------------------------------------------------------------------
    glfwSwapBuffers(window);
    glfwPollEvents();
}

void Gui::CleanUp()
{
    glfwTerminate();

    renderer.CleanUp();
}
